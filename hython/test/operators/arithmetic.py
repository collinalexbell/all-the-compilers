# Integral arithmetic
print(+5)
print(-5)

print(1+1)
print(3-5)
print(2*4)
print(3/5)
print(5%2)
print(5//2)
print(5**3)
print(0**0)

# Floating point arithmetic
print(+5.0)
print(-5.0)
print(1.0 + 2.0)
print(2.0 - 0.5)
print(1.0 * 1.6)
print(1.0 / 4.8)
print(5.0 % 2.0)
print(5.0 // 2.0)
print(5.0 ** 3.4)
print(0.0 ** 0.0)

# Mixed integral / floating point
print(1 + 2.0)
print(1.7 - 2)
print(17.24 * 4)
print(2 / 0.3)
print(5.4 % 2)
print(7 // 1.7)
print(9.1 ** 3)
print(2 ** 3.14159)

# Precedence
print(2**-1)

print(3 - 2 + 1)
print(1 + 2.2 * 3)
print((1 + 2.2) * 3)
print(3 + 5 % 2)
print(10 / 3 // 2)
print(4 + 2 ** 7)


